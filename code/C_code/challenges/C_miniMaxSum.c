/*
 * Complete the 'miniMaxSum' function below.
 *
 * Given a series of positive integers, find the
 * minimum and maximum values that can be calculated
 * by summing exactly all the number, except the 
 * maximum and minimum.
 *
 * The output must be the minimum sum, space, and the 
 * maximum sum.
 */

void miniMaxSum(int arr_count, int* arr) {

    int max = 0, min = INT_MAX;
    long sum_all = 0;
    
    // calculate the max and min
    for (int i =0; i < arr_count; i++)
    {
        if( arr[i] > max) { max = arr[i]; }
        if( arr[i] < min) { min = arr[i]; }
        sum_all += arr[i];
    }
    //printf("max: %d\n", max);
    //printf("min: %d\n", min);
    //printf("sum: %ld\n", sum_all);
    
    // the max sum, will be the sum of all elements minus the minimum element
    // the min sum, will be the sum of all elements minus the maximum elment
    long max_sum = sum_all - min;
    long min_sum = sum_all - max;
    
    printf("%ld %ld", min_sum, max_sum);
}

