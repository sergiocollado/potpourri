/*
 * Complete the 'plusMinus' function below.
 *
 * The function accepts INTEGER_ARRAY arr as parameter.
 * input and array with pisitive, negative and zero values
 * the function has to print the ratio of each groupt of numbers: positive, negative, and zeroes. 
 * print the decimal value of each number gorup (p, n, z) on a new line with 
 * 6 places of precission after the decimal. 
 
 * example input:
 * 8
 * 3 4 5 -8 -7 -6 0 0
 * expected output
 * 0.375000
 * 0.375000
 * 0.250000
 */

void plusMinus(int arr_count, int* arr) {

    float positive = 0, negative = 0, zeros = 0;
    
    //loop to count the positive, negative and zeros
    for (int i = 0; i < arr_count; i++)
    {
        if (arr[i] > 0)        { positive++; }
        else if (arr[i] < 0)   { negative++; }
        else  if (arr[i] == 0) { zeros++;}
        else { printf("error\n"); }
    }
    
    //printf("positive: %f\n", positive); 
    //printf("negative: %f\n", negative); 
    //printf("zeros: %f\n", zeros); 
    
    printf("%0.6f\n", (positive/(float)arr_count));
    printf("%0.6f\n", (negative/(float)arr_count));
    printf("%0.6f",   (zeros/(float)arr_count));
}

